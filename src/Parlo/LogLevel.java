package Parlo;

/**
 * The level of a message logged by Parlo.
 */
public enum LogLevel 
{
	/**
	 * An error message.
	 */
    error,
    
    /**
     * A warning.
     */
    warn,
    
    /**
     * An informational message.
     */
    info,
    
    /**
     * A verbose message.
     */
    verbose;
}
